public class InfoRestante {
	//Atributos	
	private String tipo;
	private String valor;
	
	
	//Metodos Construtor
	public InfoRestante(String tipo, String valor) {
		this.tipo = tipo;
		this.valor = valor;
	}
	
	//Metodo get e set
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public String getValor() {
		return valor;
	}
	public void setValor(String valor) {
		this.valor = valor;
	}

	
	

}